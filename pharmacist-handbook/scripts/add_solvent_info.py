def add_solvent_to_drugs():
    """为注射剂型药品添加溶媒选择信息"""
    drugs, header = load_drugs()
    
    if not drugs:
        return
    
    updated_count = 0
    
    for drug in drugs:
        dosage_form = drug.get('dosage_form', '')
        
        # 检查是否为注射剂型
        if is_injection(dosage_form):
            manual = drug.get('manual', {})
            
            # 检查是否已有详细信息
            if manual and manual.get('source'):
                # 添加溶媒选择信息
                solvent = get_solvent_info(drug['name'], dosage_form)
                manual['solvent'] = solvent
                updated_count += 1
                print(f"✓ 已添加溶媒信息: {drug['name']} {dosage_form}")
    
    # 保存更新后的数据
    output = header + json.dumps(drugs, ensure_ascii=False, indent=2) + '];\n'
    
    with open('data/drugs.js', 'w', encoding='utf-8') as f:
        f.write(output)
    
    print(f"\n{'='*70}")
    print(f"✅ 完成！共为 {updated_count} 个注射剂型药品添加溶媒选择信息")
    print(f"{'='*70}")

if __name__ == '__main__':
    add_solvent_to_drugs()
