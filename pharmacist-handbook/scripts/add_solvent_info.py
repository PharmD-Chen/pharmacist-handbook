def load_drugs():
    """加载药品数据"""
    with open('data/drugs.js', 'r', encoding='utf-8') as f:
        content = f.read()
    
    # 提取JSON数组
    start = content.find('[')
    end = content.rfind(']')
    if start == -1 or end == -1:
        print("错误: 无法找到JSON数据")
        return [], None
    
    return json.loads(content[start:end+1]), content[:start] + '['

def is_injection(dosage_form):
    """判断是否为注射剂型"""
    injection_keywords = ['注射', '输液', '滴注']
    return any(keyword in dosage_form for keyword in injection_keywords)

